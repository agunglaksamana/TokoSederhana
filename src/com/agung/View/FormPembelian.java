    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        try {
        String Cari = txtkodesupplier.getText();
        Statement statement = Databases.getkoneksi().createStatement();
        String sql = "Select kodesuplier,alamat from supplier where kodesuplier like '"+Cari+"'";
        ResultSet rs = statement.executeQuery(sql);
        if (rs.next()) {                
           txtkodesupplier.setText(rs.getString(1));
           txtAlamat.setText(rs.getString(2));
        }    
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "error");
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnCariActionPerformed

