    private void NoUrut() {
        try {
        String auto="";
        Statement st=Databases.getkoneksi().createStatement();
        ResultSet rs=st.executeQuery("select * from pembelian order by nopembelian asc");
        while(rs.next()){
          auto = ""+rs.getString("nopembelian");  
        }
        if (auto.equals("")){
            txtkodepembelian.setText("PB0001");
        }else{
        auto = auto.substring(2);
        int angka = Integer.parseInt(auto);
	angka = angka + 1;
	if (angka <= 9) {
		auto = "000" + angka;
	} else if (angka >= 10 && angka <= 99) {
		auto = "00" + angka;
        } else if (angka >= 100 && angka <= 999) {
    		auto = "0" + angka;
    	} else if (angka >= 1000 && angka <= 9999) {
    		auto = "" + angka;
    	}
    	if (auto.length() > 5) {
    		auto = auto.substring(auto.length() - 5, auto.length());
    	}
    	auto = "PB" + auto;
    	txtkodepembelian.setText(auto);
        }
    } catch (SQLException e) {
        Logger.getLogger(FormBarang.class.getName()).log(Level.SEVERE, null, e);
    }

    }
    
